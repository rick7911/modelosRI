    public inicio() {
        try {
            initComponents();
            palabrasRefinadas = d.listaPalabras();
            for (Palabra palabra : palabrasRefinadas) {
                lista.add(palabra);
            }
            Collections.sort(lista);
            Iterator<Palabra> iterador = lista.listIterator();
            while (iterador.hasNext()) {
                Palabra p = iterador.next();
                docs = p.getDocumentos();
                System.out.print(p.getNombre());
                for (Documento doc  : docs) {
                    if(doc.getRepeticiones()>0){
                        System.out.print("  "+doc.getNombre()+"-"+doc.getRepeticiones());
                    }
                }
                System.out.println();
            }
        } catch (IOException ex) {
            Logger.getLogger(inicio.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

