    public void imprimir(String palabraBuscar){
        ArrayList<String> parametros = new ArrayList<>();
        String token = " ";
        StringTokenizer linea = new StringTokenizer(palabraBuscar);
        while (linea.hasMoreTokens()) {
                token = linea.nextToken();
                parametros.add(token);
        }
        Collections.sort(parametros);
        for (String parametro : parametros) {
            for ( Palabra palabra : inicio.palabrasRefinadas){    
                if(palabra.getNombre().equals(parametro)){
                   ArrayList<Documento> d = new ArrayList<Documento>(palabra.getDocumentos());
                   System.out.print(palabra.getNombre() + " - ");
                    for (Documento doc : d) {
                        System.out.print(doc.getNombre() + " ");
                    }
                    System.out.println();
                }
            }
        }
        
    }
